import * as d3 from 'd3';
import { core } from '@greycat/sdk';
import '../layout';
import { TableLike } from '../../src';

const app = document.createElement('app-layout');
app.title = 'Chart (scatter)';
await app.init();

document.body.prepend(app);

let nbPoints = 10;
const randomTable = (): TableLike => {
  return {
    cols: [Array.from({ length: nbPoints }, d3.randomNormal(5, 1))],
  };
};

app.actions.prepend(
  <>
    <li>
      <a
        href="#"
        onclick={() => {
          chart.config.table = randomTable();
          chart.compute();
          chart.update();
        }}
      >
        Randomize
      </a>
    </li>
    <li>
      <a
        href="#"
        onclick={() => {
          nbPoints += 10;
          chart.config.table = randomTable();
          chart.compute();
          chart.update();
        }}
      >
        Add 10 points
      </a>
    </li>
    <li>
      <a
        href="#"
        onclick={() => {
          chart.config.cursor = !chart.config.cursor;
        }}
      >
        Toggle cursor
      </a>
    </li>
  </>,
);

const chart = document.createElement('gui-chart');
chart.style.height = '100%';
app.main.replaceChildren(chart);

chart.addEventListener('selection', (e) => {
  const from = core.time.fromMs(e.detail.from as number);
  const to = core.time.fromMs(e.detail.to as number);
  console.log(`selection from ${from} to ${to}`);
});

